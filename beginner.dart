void main() {
  // This is a simple Dart program that prints "Hello_world" to the console.

  /*   This is a multi-line comment.
   It can span multiple lines and is useful for providing detailed explanations.
  */
  print("Hello_world");

  // Dart variables
  int age; // variable declaration
  age = 25; // variable initialization
  print(age);

  // variable initialization and declaration in one line
  String name = "John";
  print(name);

  // conventional variable declaration
  int galaxies = 15;
  galaxies = 16; // variable reassignment
  print(galaxies);

  // Dart constants
  final int daysOfYear = 365;
  print(daysOfYear);
  // daysOfYear = 366; // This will cause an error as it's a constantvariable.
}
